	public static void BattleField(String nStr) {
		// now = str[x][y];
		for (int n = 0; n < nStr.length(); n++) {

//			showArr(str);

			if (nStr.charAt(n) == 'S') {
				if (flag == 0 && x - 1 >= 0) {// 상
					for (int i = x - 1; i >= 0; i--) {
						if (str[i][y] == '#')
							break;
						else if (str[i][y] == '.' || str[i][y] == '-') {
							continue;
						} else if (str[i][y] == '*') {
							str[i][y] = '.';
							break;
						}
					}
				} else if (flag == 1 && x + 1 <= str.length - 1) {// 하
					for (int i = x + 1; i < str.length; i++) {
						if (str[i][y] == '#')
							break;
						else if (str[i][y] == '.' || str[i][y] == '-')
							continue;
						else if (str[i][y] == '*') {
							str[i][y] = '.';
							break;
						}
					}
				} else if (flag == 2 && y - 1 >= 0) {// 좌
					for (int i = y - 1; i >= 0; i--) {
						if (str[x][i] == '#')
							break;
						else if (str[x][i] == '.' || str[x][i] == '-')
							continue;
						else if (str[x][i] == '*') {
							str[x][i] = '.';
							break;
						}
					}
				} else if (flag == 3 && y + 1 <= str[x].length - 1) {// 우
					for (int i = y + 1; i < str[x].length; i++) {
						if (str[x][i] == '#')
							break;
						else if (str[x][i] == '.' || str[x][i] == '-')
							continue;
						else if (str[x][i] == '*') {
							str[x][i] = '.';
							break;
						}

					}
				}
			} else if (nStr.charAt(n) == 'U') {
				if (x - 1 >= 0 && str[x - 1][y] == '.') {
					str[x - 1][y] = '^';
					str[x][y] = '.';
					x--;
				} else {
					str[x][y] = '^';
				}
				flag = 0;
			} else if (nStr.charAt(n) == 'D') {
				if (x + 1 <= str.length - 1 && str[x + 1][y] == '.') {
					str[x + 1][y] = 'v';
					str[x][y] = '.';
					x++;
				} else {
					str[x][y] = 'v';
				}
				flag = 1;
			} else if (nStr.charAt(n) == 'R') {
				if (y + 1 <= str[x].length - 1 && str[x][y + 1] == '.') {
					str[x][y + 1] = '>';
					str[x][y] = '.';
					y++;
				} else {
					str[x][y] = '>';
				}
				flag = 3;
			} else if (nStr.charAt(n) == 'L') {
				if (y - 1 >= 0 && str[x][y - 1] == '.') {
					str[x][y - 1] = '<';
					str[x][y] = '.';
					y--;
				} else {
					str[x][y] = '<';
				}
				flag = 2;
			}
		}
	}

